#include <stdint.h>
const int matrixSize = 10;

class matrixes
{
  public:
    uint16_t matrix[matrixSize];
    
    matrixes operator+(const matrixes &m) 
    {
      matrixes result;
      for(int i = 0; i < matrixSize; i++)
      {
        result.matrix[i] = matrix[i] | m.matrix[i];
      }
      return result;
    };
  private:

};

